// `koan connect` generates the full-command skill pack for host agents:
// a Claude Code skill (~/.claude/skills/koan/SKILL.md) and a Codex prompt
// (~/.codex/prompts/koan.md). The skill is instructions only — all state and
// logic stay in the MCP tools / CLI ("skill = playbook, tools = truth").
// Everything here is static strings parameterized by the host adapter; no
// network, no model calls.

import { spawnSync } from "node:child_process";
import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { KOAN_VERSION } from "../core/constants.js";
import { adapterFor, type HostId } from "../core/hostAdapter.js";

export type ConnectAgent = "claude" | "codex";

export type ExecFn = (
  cmd: string,
  args: string[]
) => { status: number | null; error?: unknown };

export interface ConnectInput {
  agents: ConnectAgent[];
  homeDir: string;
  print?: boolean;
  exec?: ExecFn;
  out?: (line: string) => void;
}

// All 17 MCP tool names as string literals. Track B adds koan_accept_clarity
// and koan_archive_goal in parallel, so we cannot import toolNames from
// src/mcp/server.ts without coupling the tracks; the completeness test in
// test/connect.test.ts keeps this list honest.
const MCP_TOOL_NAMES = [
  "koan_get_profile",
  "koan_update_profile",
  "koan_inspect_project",
  "koan_start_session",
  "koan_get_next_question",
  "koan_record_answer",
  "koan_accept_clarity",
  "koan_crystallize_documents",
  "koan_get_status",
  "koan_update_status",
  "koan_archive_goal",
  "koan_record_bright_idea",
  "koan_record_insight",
  "koan_synthesize_prd",
  "koan_prepare_qa",
  "koan_prepare_handoff",
  "koan_get_dashboard"
] as const;

export function registrationCommand(agent: ConnectAgent): string[] {
  if (agent === "claude") {
    return ["claude", "mcp", "add", "--scope", "user", "koan", "koan-mcp"];
  }
  return ["codex", "mcp", "add", "koan", "--", "koan-mcp"];
}

function generatedNotice(agent: ConnectAgent): string {
  return `Generated by koan connect v${KOAN_VERSION} — re-run \`koan connect ${agent}\` to update; do not hand-edit.`;
}

// Shared body builder: single source of truth for the journey playbook,
// rubric, routing table, fallback, and philosophy rule. Host tone comes
// exclusively from adapterFor(host) so the contract never forks per agent.
